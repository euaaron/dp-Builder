package profile;

public class SocialUrl {
    private String name;
    private String url;

    public SocialUrl() {
        this.name = "";
        this.url = "";
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
